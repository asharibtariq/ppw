                            <hr>
                           <p>Population Program Wing is Performing Following Functions:-
                               </p>
                            <p>
                               i. Develop National Population Policy Framework/Vision
<br>
                               ii. Develop National Action Plan(s) for execution of the Population Policy Framework/Vision;
                                <br>
                               iii. Coordinate with Provincial and Regional Population Welfare Departments (PWDs), NGOs, CSOs and other Organizations working in the field of Reproductive Health and Family Planning (RH&FP); especially for the implementation of National Action Plan
                                <br>
                               iv. Coordinate with International Agencies / Organizations to muster financial and technical assistance for population welfare program in Pakistan;
                                <br>
                               v. Undertake joint performance review of all implementing partners (PWDs, NGOs, CSOs and Private Sector) and provide technical and professional guidance for effective implementation of RH & FP programmes;
                                <br>
                               vi. Develop / Review National Standards and Protocols for capacity building / training on RH&FP and quality assurance for improved RH&FP Service Delivery;
                                <br>
                               vii. Coordination for the Central Warehouse & Supplies (CW&S);
                                <br>
                               viii. Coordinate the matters relating to the National Trust for Population Welfare (NATPOW) and the National Institute of Population Studies (NIPS);
                                <br>
                               ix. Coordination with the provinces/regions to ensure Contraceptive Commodity Security, Supply Chain Management and Warehousing of contraceptives for Provincial and Regional PWDs;
                                <br>
                               x. Deal with residual matters of the defunct Ministry of Population Welfare (MoPW).</p>
