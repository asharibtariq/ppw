                            <h2 class="article-title text-center">Background of Population Program Wing </h2>

                            <hr>
                            <p>

                                Population Welfare Program was devolved to the Provinces in 2010, under the 18th Constitutional Amendment. Now, provinces are responsible for formulating their respective Population Strategies and implementation of Population Welfare Programs. At the time of devolution, a wing namely Population Programme Wing was created and placed in the Planning & Development Division, Islamabad with the mandate to coordinate at the national & international level in the area of population welfare to advance the agenda of population and development in Pakistan. The Population Programme Wing was shifted from Planning & Development Division to Ministry of National Health Services, Regulations & Coordination (NHSR&C) in 2013. The Population Programme Wing is headed by Director General (Population), M/o NHSR&C.</p>

