                                The Council of Common Interests (CCI) in its meeting held on 19th November, 2018 directed Ministry of National Health Services, Regulations & Coordination (MoNHSR&C) to “prepare an action plan with financial modalities for operationalization of the recommendations in consultation with all relevant stakeholders and submit for the approval of the Federal Task Force at the earliest.” In pursuance to the above direction MoNHSR&C held a series of meetings with Secretaries/DGs of Health & Population Welfare Departments of the Provinces and AJ&K and GB and finalized an Action Plan with consensus. Other Implementing Partners from Public and Private Sector also participated in the finalization of the Action Plan. The Action Plan spells out the actions required to implement each CCI Recommendation with timelines, budget, output I impact indicators as an evidence to the implementation. It also indicates the responsible body to carry out the Action Plan and other stakeholders involved in the implementation of the particular Action. This Action Plan aims to: • Increase the present Contraceptive Prevalence Rate (CPR) of 34% to 50% by 2025 and 60% by 2030; • Reduce the present Fertility Rate of 3.6 births per woman to 2.8 births by 2025 and 2.2 birth per woman by 2030; • Lower the present Average Population Growth Rate of 2.4% to 1.5% by 2025, and to 1.1% by 2030. National Narrative on Population The CCI directed to develop a National Narrative in consultation with provinces and other stakeholders to create a sense of urgency and necessity of reducing population growth rate and achieving socio-economic wellbeing for all. In view to develop the National Narrative a study was commissioned to carry out the review of advocacy strategy in Pakistan. Similarly, another study was carried out on Pakistan Economy Analysis (PEA). Based on the outcome of the aforesaid studies, a draft National Narrative was developed and presented to the National Consultative Group. After review and revision of the National Narrative by the National Consultative Group, the revised version of the Narrative was circulated to Provincial Governments and other Implementing Partners in the public & private sector. Based on the feedback received from Provincial Governments and other Implementing Partners, a National Narrative was finalized and has been approved by the Federal Task Force in its 4th meeting held on 23rd June 2021. The theme of the National Narrative is as follows: “Parents have the right to freely and responsibly decide the number and spacing of their children to fulfill the fundamental rights of their children and family by maintaining a Balance/ Tawazun between their family size and resources. The Government and society have the responsibility to facilitate parents to achieve this balance by providing universal access to family planning information and services, thereby achieving sustainable development.”.</p>

